USE mysql;
FLUSH PRIVILEGES;
CREATE DATABASE IF NOT EXISTS $WP_DATABASE;
CREATE USER IF NOT EXISTS 'root'@'localhost' IDENTIFIED BY '$WP_PWD';
SET old_passwords = 0;
SET PASSWORD FOR 'root'@'localhost' = PASSWORD('$WP_PWD');
CREATE USER IF NOT EXISTS '$WP_ROOT'@'%' IDENTIFIED BY '$WP_PWD';
GRANT ALL PRIVILEGES ON $WP_DATABASE.* TO '$WP_ROOT'@'%';
FLUSH PRIVILEGES;
SELECT 'MySQL initialization finished!' INTO OUTFILE '/var/lib/mysql/mysql_initialized';