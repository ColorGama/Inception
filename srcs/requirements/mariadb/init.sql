USE mysql;
FLUSH PRIVILEGES;
CREATE DATABASE IF NOT EXISTS $DATABASE;
CREATE USER IF NOT EXISTS 'root'@'localhost' IDENTIFIED BY '$ROOT_PWD';
SET old_passwords = 0;
SET PASSWORD FOR 'root'@'localhost' = PASSWORD('$ROOT_PWD');
CREATE USER IF NOT EXISTS '$USER'@'%' IDENTIFIED BY '$U_PWD';
GRANT ALL PRIVILEGES ON $DATABASE.* TO '$USER'@'%';
FLUSH PRIVILEGES;
SELECT 'MySQL initialization finished!' INTO OUTFILE '/var/lib/mysql/mysql_initialized';